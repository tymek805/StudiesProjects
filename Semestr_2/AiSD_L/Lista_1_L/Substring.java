package AiSD_L.Lista_1_L;

public class Substring {
    private int pos = -1;
    private int length = 0;

    public void setLength(int length) {
        this.length = length;
    }

    public int getLength() {
        return length;
    }

    public void setPos(int pos) {
        this.pos = pos;
    }

    public int getPos() {
        return pos;
    }
}
